puts "Travail : #{10 * 5 * 11}" # #{]} affiche dans le string le résultat de l'opération en heure
puts "En minutes ça fait : #{10 * 5 * 11 * 60}" # affiche le résultat en minutes

puts "Et en secondes ?" #affiche un string

puts 10 * 5 * 11 * 60 * 60 # affiche le résultat en secondes

puts "Est-ce que c'est vrai que 3 + 2 < 5 - 7 ?" #affiche uniquement un string

puts 3 + 2 < 5 - 7 # Affiche le résultat booléen de l'opération sui est fausse

puts "Ça fait combien 3 + 2 ? #{3 + 2}" #affiche le string + l'opération
puts "Ça fait combien 5 - 7 ? #{5 - 7}"# pareil que précédemment

puts "Ok, c'est faux alors !" # string

puts "C'est drôle ça, faisons-en plus :" #string

puts "Est-ce que 5 est plus grand que -2 ? #{5 > -2}" # string + test l'égalité et affiche true (puisque l'opération est vraie)
puts "Est-ce que 5 est supérieur ou égal à -2 ? #{5 >= -2}" # pareil
puts "Est-ce que 5 est inférieur ou égal à -2 ? #{5 <= -2}" # string + test l'égalité et affiche false (puisque l'opération est fausse)

